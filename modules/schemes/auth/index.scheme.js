const mongoose = require('mongoose');
const Schema = mongoose.Schema;

const SchemeAuth = new Schema({
    first_name: {
        type: String,
        required: true,
        trim: true,
    },
    last_name: {
        type: String,
        required: true,
        trim: true,
    },
    email: {
        type: String,
        required: true,
        trim: true,
    },
    password: {
        type: String,
        required: true,
        trim: true,
    },
}, {timestamps: true,});

module.exports = SchemeAuth;